/// Tailwind CSS version enumeration
///
/// Represents different versions of Tailwind CSS color palettes.
enum TailwindVersion {
  /// Tailwind CSS v2.0
  v2_0,

  /// Tailwind CSS v3.0
  v3_0,

  /// Tailwind CSS v3.1
  v3_1,

  /// Tailwind CSS v3.2
  v3_2,

  /// Tailwind CSS v3.3
  v3_3,

  /// Tailwind CSS v3.4
  v3_4,

  /// Tailwind CSS v4.0
  v4_0,

  /// Tailwind CSS v4.1
  v4_1,

  /// Tailwind CSS v4.2 (default)
  v4_2,

  /// Latest version (currently v4.2)
  latest,
}
